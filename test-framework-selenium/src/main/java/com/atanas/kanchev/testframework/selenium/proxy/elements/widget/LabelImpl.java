package com.atanas.kanchev.testframework.selenium.proxy.elements.widget;

import com.atanas.kanchev.testframework.selenium.proxy.elements.base.ElementImpl;
import org.openqa.selenium.WebElement;


/**
 * Wraps a label on a html form with some behavior.
 */
public class LabelImpl extends ElementImpl implements Label {

    /**
     * Creates a Element for a given WebElement.
     *
     * @param element element to wrap up
     */
    public LabelImpl(WebElement element) {
        super(element);
    }

    @Override
    public String getFor() {
        return getWrappedElement().getAttribute("for");
    }
}
